fn main_if() {
    let number = 12;

//interesting thing to note for if expressions is that the condition must be a bool
    if number < 5 {
       println!("number is less than 5");
    } else if number < 10{
        println!("number is less than 10");
    } else{
         println!("number is greater than 10");
    }
}


